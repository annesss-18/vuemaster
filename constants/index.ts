export const feedbackSchema = z.object({
  totalScore: z.number(),
  // Use a fixed-length array instead of a tuple to avoid generating
  // a response schema that starts lists where the API expects a single
  // value. The generative API's proto mapping doesn't accept the
  // tuple->items translation used previously.
  // Provide named category fields so scores + comments are returned
  // as explicit properties. This makes it simpler to access each
  // category individually and avoids ambiguous list/tuple schema
  // translation issues with the generative API.
  categoryScores: z.object({
    communicationSkills: z.object({
      score: z.number(),
      comment: z.string(),
    }),
    technicalKnowledge: z.object({
      score: z.number(),
      comment: z.string(),
    }),
    problemSolving: z.object({
      score: z.number(),
      comment: z.string(),
    }),
    culturalFit: z.object({
      score: z.number(),
      comment: z.string(),
    }),
    confidenceAndClarity: z.object({
      score: z.number(),
      comment: z.string(),
    }),
  }),
  strengths: z.array(z.string()),
  areasForImprovement: z.array(z.string()),
  finalAssessment: z.string(),
});

